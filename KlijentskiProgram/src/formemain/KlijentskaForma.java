    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            if (txtOpis.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti prijavu");
                return;
            }
            Klijent k = (Klijent) cmbKlijent.getSelectedItem();
            String datumPrijave = sdf.format(new Date());
            PrijavaKvara pk = new PrijavaKvara(-1, txtOpis.getText(), sdf.parse(datumPrijave), 1, sesija.Sesija.getInstance().getUlogovani(), k, null);
            KlijentKontroler.getInstance().sacuvajPrijavu(pk);
            JOptionPane.showMessageDialog(this,"Sistem je sacuvao prijavu");
            resetujFormu();

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this,"Greska prilikom parsiranja!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }


    }//GEN-LAST:event_jButton2ActionPerformed

