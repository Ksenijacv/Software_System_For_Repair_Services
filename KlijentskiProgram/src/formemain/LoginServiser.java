    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            String username = txtUsername.getText();
            String password = new String(txtPassword.getPassword());
            
            if (username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Molimo Vas, unesite Vasu "
                        + "lozinku i korisnicko ime!");
                return;
            }
            
            if( !username.matches("[A-Z][a-zA-Z]+")){
                 JOptionPane.showMessageDialog(this, "Username mora sadrzati pocetno "
                 + "veliko slovo i samo slova da sadrzi");
                return;
            }
            if(!password.matches("[a-zA-Z]+[0-9]+")){
                JOptionPane.showMessageDialog(this, "Password mora sadrzati bar 1 slovo na pocetku i "
                 + "bar jedan broj na kraju");
                return;
            }
            Serviser s=new Serviser();
            s.setKorisnickoIme(username);
            s.setLozinka(password);
            
            s=KlijentKontroler.getInstance().login(s);
            JOptionPane.showMessageDialog(this,"Sistem je pronasao servisera!");
            Sesija.getInstance().setUlogovani(s);
            new KlijentskaForma().setVisible(true);
            this.dispose();
            
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Nepostojeca baza podataka, morate konfigurisati bazu na serverskoj formi!");
        } catch (Exception e) {
            if (e instanceof SocketException) {
                JOptionPane.showMessageDialog(rootPane, "Komunikacija izmedju servera je prekinuta");
                this.dispose();
                System.exit(0);
            } else {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
                e.printStackTrace();
            }
        }
        

    }//GEN-LAST:event_btnLoginActionPerformed

