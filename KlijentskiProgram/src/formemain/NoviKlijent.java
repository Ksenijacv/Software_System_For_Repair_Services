    private void popuni() {
        try {
            cmbMestp.removeAllItems();

            ArrayList<Mesto> mest = KlijentKontroler.getInstance().vratiMesta();

            for (Mesto mesto : mest) {
                cmbMestp.addItem(mesto);
            }
        } catch (Exception ex) {
            Logger.getLogger(NoviKlijent.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
