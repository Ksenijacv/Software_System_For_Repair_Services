    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            if (txtImePrezime.getText().isEmpty()
                    || txtKontakt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
                return;
            }

            Mesto m = (Mesto) cmbMestp.getSelectedItem();

            KlijentKontroler.getInstance().sacuvajKlijenta(
                    new Klijent(-1, txtImePrezime.getText(), txtKontakt.getText(), m, Sesija.getInstance().getUlogovani())
            );
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio klijenta");
            KlijentskaForma kf = (KlijentskaForma) getParent();
            kf.popuniCombo();
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem nije zapamtio klijenta. " + ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

